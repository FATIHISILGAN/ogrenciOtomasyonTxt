    private void ekle_btnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ekle_btnActionPerformed
           
           String Ogrencid = İd_txt.getText();
                  
                   
        String kullaniciAd =  kullaniciAd_txt.getText();
                
        String parola= parola_txt.getText();
                
        
                           
        String ogrenciAd =  Ad_txt.getText();
                
        String ogrenciSoyad= Soyad_txt.getText();
        
                           
        String tc =  Tc_txt.getText();
                
        String dogumTarihi= doğumTarihi_txt.getText();
        
                           
        String sinifİd=  sınıfİd_txt.getText();
                
        
      
        try {
            FileWriter writer = new FileWriter("ogrenciler.txt",true);            
            writer.write("" + Ogrencid + " / " + kullaniciAd + " / " + parola + " / " + ogrenciAd+  " / " + ogrenciSoyad+  " / "+ tc + " / "+dogumTarihi + " / "+sinifİd+ "\n");        
            writer.close();
            JOptionPane.showMessageDialog(null, "eklendi");
                 
        }
        catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }       
              
    }//GEN-LAST:event_ekle_btnActionPerformed

